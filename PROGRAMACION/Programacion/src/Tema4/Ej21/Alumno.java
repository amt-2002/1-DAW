package Tema4.Ej21;

public class Alumno extends Persona {
	
	private String dni;
	private Double nota;

	public Alumno() {

	}
	
	public String getDni() {
		return dni;
	}

	public void setDni(String dni) {
		this.dni = dni;
	}

	public Double getNota() {
		return nota;
	}

	public void setNota(Double nota) {
		this.nota = nota;
	}

	
}